    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            double dist = Double.parseDouble(JOptionPane.showInputDialog(null, "Insira a Dist√¢ncia entre o cliente e a filial"));
            Cliente cliente = new Cliente(
                    txtEmail.getText(),
                    txt_CPF_CNPJ.getText(),
                    txtCEP.getText(),
                    txtEndereco.getText(),
                    txtBairro.getText(),
                    txtCidade.getText(),
                    cbUf.getSelectedItem().toString(),
                    labelBelt.getText(),
                    dist,
                    txtNome.getText(),
                    txtTelefone.getText(),
                    txtCelular.getText()
            );
            cliente.setCodigo(Integer.parseInt(labelID.getText()));
            daoCliente.save(cliente);

            daoTelefone.saveCelular(cliente);
            daoTelefone.saveTelefone(cliente);

            JOptionPane.showMessageDialog(null, "Cliente inclu√≠do com sucesso");

            txtEmail.setText("");
            txt_CPF_CNPJ.setText("");
            txtCEP.setText("");
            txtEndereco.setText("");
            txtBairro.setText("");
            txtCidade.setText("");
            cbUf.setSelectedIndex(0);
            labelBelt.setText("");
            txtNome.setText("");
            txtTelefone.setText("");
            txtCelular.setText("");

        } catch (Exception erro) {
            erro.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed


